#include "ft_printf.h"

void	ft_putstr(char *s, int *len)
{
	int	i;

	i = 0;
	if (s)
	{
		while (s[i] != '\0')
		{
			write(1, &s[i], 1);
			*len = *len + 1;
			i++;
		}
	}
	else
	{
		write(1, "(null)", 6);
		*len = *len + 6;
	}
}
