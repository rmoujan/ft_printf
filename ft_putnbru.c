#include "ft_printf.h"

void	ft_putnbru(unsigned int n, int *len)
{
	if (n >= 10)
	{
		ft_putnbru((n / 10), len);
		ft_putnbru((n % 10), len);
	}
	else
	{
		ft_putchar((n + '0'), len);
	}
}
